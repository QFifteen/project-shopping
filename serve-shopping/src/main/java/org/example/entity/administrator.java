package org.example.entity;

public class administrator {
    private int Administrator_id;
    private String Administrator_name;
    private int Administrator_pass;
    private String Administrator_img;
    private int Administrator_grade;

    public int getAdministrator_id() {
        return Administrator_id;
    }

    public void setAdministrator_id(int administrator_id) {
        Administrator_id = administrator_id;
    }

    public String getAdministrator_name() {
        return Administrator_name;
    }

    public void setAdministrator_name(String administrator_name) {
        Administrator_name = administrator_name;
    }

    public int getAdministrator_pass() {
        return Administrator_pass;
    }

    public void setAdministrator_pass(int administrator_pass) {
        Administrator_pass = administrator_pass;
    }

    public String getAdministrator_img() {
        return Administrator_img;
    }

    public void setAdministrator_img(String administrator_img) {
        Administrator_img = administrator_img;
    }

    public int getAdministrator_grade() {
        return Administrator_grade;
    }

    public void setAdministrator_grade(int administrator_grade) {
        Administrator_grade = administrator_grade;
    }
}
